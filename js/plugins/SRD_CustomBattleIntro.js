var SRD = SRD || {};
SRD.CustomBattleIntro = SRD.CustomBattleIntro || {};

var Imported = Imported || {};
Imported["SumRndmDde Custom Battle Intro"] = true;

(function(_) {

	_.commands = [];
	for(var i = 0; i < 360; i++) {
		_.commands[i] = String(PluginManager.parameters('SRD_CustomBattleIntro')['Frame ' + (i+1) + ' Action']);
		if(_.commands[i].match(/startBattle/i)) break;
	}

	_.hideCharacters = String(PluginManager.parameters('SRD_CustomBattleIntro')['Hide Characters']).trim().toLowerCase() === 'true';
	_.useFormula = String(PluginManager.parameters('SRD_CustomBattleIntro')['Use Zoom Formula?']).trim().toLowerCase() === 'true';
	_.formula = String(PluginManager.parameters('SRD_CustomBattleIntro')['Zoom Formula']);
	
	var _Scene_Map_startEncounterEffect = Scene_Map.prototype.startEncounterEffect;
	Scene_Map.prototype.startEncounterEffect = function() {
	    _Scene_Map_startEncounterEffect.call(this);
	    if(!_.hideCharacters) this._spriteset.showCharacters();
	};

	Scene_Map.prototype.updateEncounterEffect = function() {
	    if (this._encounterEffectDuration > 0) {
	        this._encounterEffectDuration --;
	        var speed = this.encounterEffectSpeed();
	        var n = speed - this._encounterEffectDuration;
	        var p = n / speed;
	        var q = eval(_.formula);
	        var zoomX = $gamePlayer.screenX();
	        var zoomY = $gamePlayer.screenY() - 24;
	        if(_.useFormula) $gameScreen.setZoom(zoomX, zoomY, q);

	        var s = _.commands[n];
	        if(!s) return;
	        if(s.trim().length <= 0) return;
	        //resetZoom
	        if(s.match(/resetZoom/i)) {
	        	$gameScreen.setZoom(zoomX, zoomY, 1);
	        	$gameScreen.startZoom(zoomX, zoomY, 1, 0);
	        }

	        //Zoom(scale, x, y)
	        if(s.match(/zoom\(\s*(\d+)\s*,\s*(.+)\s*,\s*(.+)\s*\)/i)) { 
	        	$gameScreen.setZoom(eval(RegExp.$2), eval(RegExp.$3), Number(RegExp.$1));
	        }
	        //Zoom(scale)
	        else if(s.match(/zoom\(\s*(\d+)\s*\)/i)) $gameScreen.setZoom(zoomX, zoomY, Number(RegExp.$1));
	        
	        //MoveZoom(scale, frames, x, y)
	        if(s.match(/moveZoom\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(.+)\s*,\s*(.+)\s*\)/i)) {
	        	$gameScreen.startZoom(eval(RegExp.$3), eval(RegExp.$4), Number(RegExp.$1), Number(RegExp.$2));
	        }
	        //MoveZoom(scale, frames)
	        else if(s.match(/moveZoom\(\s*(\d+)\s*,\s*(\d+)\s*\)/i)) $gameScreen.startZoom(zoomX, zoomY, Number(RegExp.$1), Number(RegExp.$2));

	        //flash(frames, color)
	        if(s.match(/flash\(\s*(\d+)\s*,\s*(.+)\s*\)/i)) {
	        	var r = parseInt(((RegExp.$2).substring(1,7)).substring(0,2),16);
	        	var g = parseInt(((RegExp.$2).substring(1,7)).substring(2,4),16);
	        	var b = parseInt(((RegExp.$2).substring(1,7)).substring(4,6),16);
	        	$gameScreen.startFlash([r,g,b,255], Number(RegExp.$1));
	        }
	        //flash(frames)
	        else if(s.match(/flash\(\s*(\d+)\s*\)/i)) this.startFlashForEncounter(Number(RegExp.$1));
	        //flash
	        else if(s.match(/flash/i)) this.startFlashForEncounter(speed / 2);

	        //fade(frames)
	        if(s.match(/fade\(\s*(\d+)\s*\)/i)) this.startFadeOut(Number(RegExp.$1));
	        //fade
	        else if(s.match(/fade/i)) this.startFadeOut(this.fadeSpeed());

	        //fadeWhite(frames)
	        if(s.match(/fadeWhite\(\s*(\d+)\s*\)/i)) this.startFadeOut(Number(RegExp.$1), true);
	        //fadeWhite
	        else if(s.match(/fadeWhite/i)) this.startFadeOut(this.fadeSpeed(), true);

	        //shake(power, speed, frames)
	        if(s.match(/shake\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*\)/i)) $gameScreen.startShake(Number(RegExp.$1), Number(RegExp.$2), Number(RegExp.$3));
	        
	        //snapBattleBack
	        if(s.match(/snapBattleBack/i)) this.snapForBattleBackground();
			//startBattleMusic
	        if(s.match(/startBattleMusic/i)) BattleManager.playBattleBgm();
	    }
	};

	Scene_Map.prototype.encounterEffectSpeed = function() {
	    return _.commands.length;
	};

	Spriteset_Map.prototype.showCharacters = function() {
	    for (var i = 0; i < this._characterSprites.length; i++) {
	        var sprite = this._characterSprites[i];
	        if (!sprite.isTile()) {
	            sprite.show();
	        }
	    }
	};

})(SRD.CustomBattleIntro);